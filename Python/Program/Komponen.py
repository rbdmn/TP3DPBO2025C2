class Komponen:
    def __init__(self, merk, nama, jenis_komponen):
        self.merk = merk
        self.nama = nama
        self.jenis_komponen = jenis_komponen

    def get_merk(self):
        return self.merk

    def get_nama(self):
        return self.nama
    
    def get_jenis_komponen(self):
        return self.jenis_komponen